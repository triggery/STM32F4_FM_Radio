/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint16_t encoderValueOld = encoderValue;
  uint8_t cntTick = 0;
  uint8_t direction = DIRECTION_NONE;

  //char freqBuff[20];
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  MX_TIM8_Init();
  HAL_TIM_Encoder_Start(&htim8, TIM_CHANNEL_ALL);

  setFrequency(freq);

  ssd1306_Init();

  printFreqStr(freq);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  encoderValue = TIM8->CNT;
	  if ( (encoderValue != encoderValueOld) && (cntTick < 4) ) {

		  cntTick++;

		  if( encoderValue > encoderValueOld ) {
			  direction = DIRECTION_UP;
		  }
		  else if ( encoderValue < encoderValueOld ) {
			  direction = DIRECTION_DOWN;
		  }

		  encoderValueOld = encoderValue;
	  }
	  else if ( cntTick == 4 ) {
		  cntTick = 0;

		  if( direction == DIRECTION_UP ) {
			  if(freq < FM_MAX_FREQ) {
				  freq += 0.1;
			  }
		  }
		  else if ( direction == DIRECTION_DOWN ) {
			  if(freq > FM_MIN_FREQ) {
				  freq -= 0.1;
			  }
		  }

		  setFrequency(freq);
		  printFreqStr(freq);
	  }
  }
  /* USER CODE END 3 */
}

